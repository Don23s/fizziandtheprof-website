const SUPPORTED_LANGS = ["de", "en", "fr", "es"];

function detectLang() {
  const stored = localStorage.getItem("fnp_lang");
  if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
  const browser = (navigator.language || "en").slice(0, 2).toLowerCase();
  return SUPPORTED_LANGS.includes(browser) ? browser : "en";
}

function setLang(lang) {
  localStorage.setItem("fnp_lang", lang);
  applyTranslations(lang);
  document.documentElement.lang = lang;
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
}

function applyTranslations(lang) {
  const t = translations[lang] || translations["en"];
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });
  // Swap titelbild src
  const titelbild = document.getElementById("titelbild");
  if (titelbild) titelbild.src = `assets/titelbild_${lang}.png`;
}

function initI18n() {
  const lang = detectLang();
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => setLang(btn.dataset.lang));
  });
  setLang(lang);
}
